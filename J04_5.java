public class J04_5 {
    // 실행 함수
    public static void main(String[] args) {
        // 초기 변수값
        int i = 0;
        // i<10가 참이면 스택 영역 실행
        while (i < 10) {
            // 콘솔 출력
            System
                .out
                .println("i = " + i);
            // i변수 값 증가
            i++;
        }
    }

}
