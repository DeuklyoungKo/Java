public class J02_3 {
    // 실행 함수
    public static void main(String[] args) {
        // 정수 선언
        int a = 5;
        Integer b = 10;
        // a(5) + b(10)를 더하면 15가 되어 c변수에 저장
        int c = a + b;
        // 콘솔 출력
        System
            .out
            .println("a + b = " + c);
        // 문자열 선언
        String d = "Hello";
        String e = "World";
        // d(Hello)와 f(World)의 문자열이 합쳐서 HelloWorld
        String f = d + e;
        // 콘솔 출력
        System
            .out
            .println("d + f = " + f);
    }

}
